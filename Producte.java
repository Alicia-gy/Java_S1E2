package n1e1;

public class Producte {
	
	private String name;
	private int price;
	
	public Producte (String name, int price) {
		this.name = name;
		this.price = price;
	}
	
	//methods
	
	public String getName() {
		return name;
	}
	
	public int getPrice() {
		return price;
	}

}
